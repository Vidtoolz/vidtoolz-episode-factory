/*
 * VIDTOOLZ project-scoped I2V (image-to-video) prompt generation (pure module).
 *
 * Turns a project's APPROVED final script + its SELECTED images into one
 * image-to-video motion prompt PER selected image, in the canonical
 * video-prompts.json shape that PRESTO submit (readPackageVideoPrompts) and the
 * resolver (readI2vPromptCount) already expect:
 *
 *   { version, prompt_type:'image_to_video', prompt_provider, prompt_host,
 *     source, external_copy_allowed, prompts:[{ prompt_index, prompt, ... }] }
 *
 * The LLM call itself stays in the server (callOllamaChat -> local Ollama on
 * PRESTO, no fallback to vidnux/cloud). This module only builds the instruction
 * and parses/normalizes/validates the model output. It does NOT generate videos
 * and never submits to PRESTO ComfyUI.
 *
 * Routing doctrine (enforced by the server + media-routing.js): I2V prompts are
 * generated by Ollama on PRESTO only. If PRESTO Ollama is down the caller
 * surfaces a 503 blocked state — never a fallback.
 */

const MIN_PROMPT_LEN = 25;
const MAX_SCRIPT_CHARS = 6000;
const MAX_SOURCE_PROMPT_CHARS = 400;

// Motion doctrine baked into every instruction. Wan2.2 I2V wants subtle,
// stable, presenter-background-friendly motion — not fast cuts or morphing.
const MOTION_RULES = [
  'Animate the EXISTING still image — do not invent a new scene or change the subject identity.',
  'Preserve composition, framing, lighting, and every key object in the source image.',
  'Add only subtle cinematic motion over ~2-3 seconds: slow push-in, gentle parallax, slight camera drift, soft light movement, atmospheric motion (dust, steam, haze), or a small natural human gesture where a person is present.',
  'Keep it usable as a presenter background: vertical 1080x1920, calm, no fast cuts, no hard zooms.',
  'NO text, captions, signs, logos, or UI may appear or animate in.',
  'NO morphing, melting, warping, extra limbs, face distortion, or identity drift.',
  'Avoid camera moves the still cannot support (e.g. no orbit/dolly-around of a flat scene).',
  'Describe camera motion and subject/scene motion only — present tense, concrete, concise. No dialogue, audio, scene numbers, or commentary.',
];

function clampStr(s, n) {
  return String(s == null ? '' : s).slice(0, n);
}

// Build the PRESTO-Ollama instruction: one motion prompt per SELECTED image,
// each keyed to that selection's prompt_index so the output maps 1:1.
function buildI2vPromptRequest(ctx = {}) {
  const selections = Array.isArray(ctx.selections) ? ctx.selections : [];
  const script = clampStr(ctx.script, MAX_SCRIPT_CHARS);
  const system = [
    'You write image-to-video (I2V) motion prompts for VIDTOOLZ — vertical YouTube videos (9:16) for solo AI-video creators.',
    'You are given an approved script and a set of already-chosen still images. Write ONE motion prompt for EACH image that animates THAT specific still for a Wan2.2 image-to-video clip behind an on-camera presenter.',
    '',
    'HARD RULES for every prompt:',
    ...MOTION_RULES.map((r) => `- ${r}`),
    '',
    'Return STRICT JSON only — no prose, no markdown fences. Output exactly one object per provided image, keyed by its prompt_index.',
  ].join('\n');

  const shotLines = selections.map((s) => {
    const idx = Number(s && s.prompt_index);
    const label = String((s && (s.label || s.selected_path)) || '').trim();
    const src = clampStr(s && s.prompt, MAX_SOURCE_PROMPT_CHARS);
    return `image prompt_index ${Number.isInteger(idx) ? idx : '?'} (${label}): ${src || '(no source prompt recorded)'}`;
  }).join('\n');

  const user = [
    `Project title: ${ctx.title || ''}`,
    ctx.premise ? `Topic / premise: ${ctx.premise}` : '',
    ctx.scoreSummary ? `Why it matters: ${ctx.scoreSummary}` : '',
    '',
    'APPROVED SCRIPT (for tone/energy only — keep motion subtle regardless):',
    script || '(script unavailable)',
    '',
    `SELECTED IMAGES (${selections.length}) — write one motion prompt for each, matching its own scene:`,
    shotLines || '(no selected images)',
    '',
    `Return JSON: {"prompts":[{"prompt_index","i2v_prompt","motion_intent","camera_motion","subject_motion"}]}`,
    'prompt_index: copy the image prompt_index above. i2v_prompt: the motion description for THAT image (the rules above are appended by the system; describe motion only).',
    'motion_intent: one short phrase. camera_motion: e.g. "slow push-in". subject_motion: e.g. "gentle dust drift".',
    `Return exactly ${selections.length} objects, one per prompt_index, no duplicates.`,
  ].filter((l) => l !== '').join('\n');

  const schema = {
    type: 'object',
    properties: {
      prompts: {
        type: 'array',
        items: {
          type: 'object',
          properties: {
            prompt_index: { type: 'number' },
            i2v_prompt: { type: 'string' },
            motion_intent: { type: 'string' },
            camera_motion: { type: 'string' },
            subject_motion: { type: 'string' },
          },
          required: ['i2v_prompt'],
        },
      },
    },
    required: ['prompts'],
  };
  return { system, user, schema };
}

function stripFences(content) {
  return String(content == null ? '' : content).replace(/```(?:json)?/gi, '').trim();
}

function pickI2vText(item) {
  if (!item || typeof item !== 'object') return '';
  return String(item.i2v_prompt || item.prompt || '').trim();
}

// Parse + normalize + validate the model output into one canonical record per
// SELECTED image, mapped by prompt_index (positional fallback). Throws 502 (no
// partial write) when the model can't supply a usable prompt for every image.
function parseI2vPrompts(content, selections, ctx = {}) {
  const sel = Array.isArray(selections) ? selections : [];
  if (!sel.length) {
    const e = new Error('No selected images to generate I2V prompts for.');
    e.statusCode = 400; throw e;
  }
  let parsed;
  try { parsed = typeof content === 'string' ? JSON.parse(stripFences(content)) : content; } catch (e) {
    const err = new Error('PRESTO Ollama did not return valid JSON. Try again, or set OLLAMA_PRESTO_MODEL to another installed model.');
    err.statusCode = 502; throw err;
  }
  const list = Array.isArray(parsed) ? parsed
    : (parsed && Array.isArray(parsed.prompts) ? parsed.prompts
      : (parsed && Array.isArray(parsed.video_prompts) ? parsed.video_prompts : null));
  if (!list) { const e = new Error('Model returned no prompts array.'); e.statusCode = 502; throw e; }

  // Index model items by their declared prompt_index for strict 1:1 mapping.
  const byIndex = new Map();
  list.forEach((it) => {
    const n = Number(it && it.prompt_index);
    if (Number.isInteger(n) && !byIndex.has(n)) byIndex.set(n, it);
  });
  // If the model keyed its items by prompt_index, require a keyed match for every
  // selection (a missing key = a missing prompt). Only fall back to positional
  // mapping when the model returned an unkeyed array.
  const keyed = byIndex.size > 0;

  const nowIso = ctx.nowIso || new Date().toISOString();
  const records = [];
  const missing = [];
  for (let i = 0; i < sel.length; i++) {
    const s = sel[i] || {};
    const promptIndex = Number.isInteger(Number(s.prompt_index)) ? Number(s.prompt_index) : (i + 1);
    const item = keyed ? byIndex.get(promptIndex) : list[i];
    const text = pickI2vText(item);
    if (text.length < MIN_PROMPT_LEN) { missing.push(promptIndex); continue; }
    const variant = String(s.selected_source || s.source || '').trim();
    records.push({
      index: i + 1,
      prompt_index: promptIndex,
      source_image: String(s.selected_path || s.path || '').trim(),
      source_image_variant: variant,
      image_prompt: clampStr(s.prompt, MAX_SOURCE_PROMPT_CHARS),
      prompt: text,                 // canonical text consumed by PRESTO submit
      i2v_prompt: text,             // explicit mirror for the editor UI
      motion_intent: String((item && item.motion_intent) || '').trim(),
      camera_motion: String((item && item.camera_motion) || '').trim(),
      subject_motion: String((item && item.subject_motion) || '').trim(),
      provider: 'ollama',
      provider_host: 'presto',
      source: 'local_ollama_presto',
      generated_from: 'selected_images_and_approved_script',
      generated_at: nowIso,
      project_id: ctx.projectId || '',
    });
  }
  if (missing.length) {
    const e = new Error(`PRESTO Ollama did not return a usable I2V prompt for ${missing.length} of ${sel.length} selected image(s) (prompt_index: ${missing.join(', ')}). Nothing was written — try again.`);
    e.statusCode = 502; throw e;
  }
  return records;
}

// Canonical video-prompts.json object. Top-level shape matches saveI2vPrompts so
// PRESTO submit + the resolver read it unchanged; per-prompt records carry the
// extra review fields (harmless to those readers, which use prompt_index/prompt).
function buildVideoPromptsFile(records, ctx = {}) {
  return {
    version: 1,
    prompt_type: 'image_to_video',
    prompt_provider: 'ollama',
    prompt_host: 'presto',
    source: 'local_ollama_presto',
    external_copy_allowed: true,
    generator: 'project-i2v-prompts',
    generated_from: 'selected_images_and_approved_script',
    generated_at: ctx.nowIso || new Date().toISOString(),
    project_id: ctx.projectId || '',
    model: ctx.model || '',
    prompts: records,
  };
}

function buildManifest(records, ctx = {}) {
  return {
    generator: 'project-i2v-prompts',
    provider: 'ollama',
    provider_host: 'presto',
    model: ctx.model || '',
    source: 'local_ollama_presto',
    generated_from: 'selected_images_and_approved_script',
    generated_at: ctx.nowIso || new Date().toISOString(),
    project_id: ctx.projectId || '',
    script_path: ctx.scriptPath || '',
    selected_images: Array.isArray(records) ? records.length : 0,
    prompt_count: Array.isArray(records) ? records.length : 0,
  };
}

// Validate + normalize operator-edited prompts for the save endpoint. Requires
// one non-empty prompt per selected image, mapped by prompt_index when present.
// Throws 400 (nothing written) on a bad batch.
function normalizeSaveRecords(prompts, selections, ctx = {}) {
  const sel = Array.isArray(selections) ? selections : [];
  const incoming = Array.isArray(prompts) ? prompts : [];
  if (!sel.length) { const e = new Error('No selected images for this project.'); e.statusCode = 400; throw e; }

  const byIndex = new Map();
  incoming.forEach((p, i) => {
    if (!p) return;
    const n = Number(typeof p === 'object' ? p.prompt_index : NaN);
    if (Number.isInteger(n) && !byIndex.has(n)) byIndex.set(n, p);
  });

  const nowIso = ctx.nowIso || new Date().toISOString();
  const records = [];
  const empty = [];
  for (let i = 0; i < sel.length; i++) {
    const s = sel[i] || {};
    const promptIndex = Number.isInteger(Number(s.prompt_index)) ? Number(s.prompt_index) : (i + 1);
    let p = byIndex.get(promptIndex);
    if (p === undefined) p = incoming[i];
    const text = (p && typeof p === 'object') ? String(p.i2v_prompt || p.prompt || '').trim() : String(p || '').trim();
    if (!text) { empty.push(promptIndex); continue; }
    records.push({
      index: i + 1,
      prompt_index: promptIndex,
      source_image: String(s.selected_path || s.path || '').trim(),
      source_image_variant: String(s.selected_source || s.source || '').trim(),
      image_prompt: clampStr(s.prompt, MAX_SOURCE_PROMPT_CHARS),
      prompt: text,
      i2v_prompt: text,
      motion_intent: (p && typeof p === 'object') ? String(p.motion_intent || '').trim() : '',
      camera_motion: (p && typeof p === 'object') ? String(p.camera_motion || '').trim() : '',
      subject_motion: (p && typeof p === 'object') ? String(p.subject_motion || '').trim() : '',
      provider: 'ollama',
      provider_host: 'presto',
      source: 'local_ollama_presto',
      generated_from: 'selected_images_and_approved_script',
      generated_at: nowIso,
      project_id: ctx.projectId || '',
    });
  }
  if (empty.length) {
    const e = new Error(`Every selected image needs an I2V prompt; ${empty.length} empty (prompt_index: ${empty.join(', ')}).`);
    e.statusCode = 400; throw e;
  }
  return records;
}

module.exports = {
  MIN_PROMPT_LEN,
  MOTION_RULES,
  buildI2vPromptRequest,
  parseI2vPrompts,
  buildVideoPromptsFile,
  buildManifest,
  normalizeSaveRecords,
};
