function getAction(taskId) {
  return REGISTRY[taskId] || null;
}

// Resolve an action for a concrete project: fills the package id into endpoints
// and returns a plain object the UI can act on. Never returns a shell command.
function resolveAction(taskId, projectId) {
  const a = getAction(taskId);
  if (!a) return null;
  const out = {
    id: taskId,
    label: a.label,
    type: a.type,
    can_run_in_gui: true,
    requires: a.requires || [],
    evidence: a.evidence || [],
  };
  if (a.note) out.note = a.note;
  if (a.type === 'open') {
    const page = a.page;
    // Emit BOTH the canonical `package` and legacy `package_id` so every target
    // page works regardless of which it reads (the aigen editors read `package`;
    // the project pages read `id`/`package_id`). Fixes the image-prompts-editor
    // "Missing ?package=" bug at the source.
    const e = encodeURIComponent(projectId);
    const sep = page.includes('?') ? '&' : '?';
    out.href = `${page}${sep}package=${e}&package_id=${e}&id=${e}`;
  } else {
    out.method = a.method;
    out.endpoint = a.endpoint;
    out.dry_run = Boolean(a.dry_run);
    out.nonce = Boolean(a.nonce);
    out.body = Object.assign({ package_id: projectId }, a.body || {});
  }
  return out;
}

module.exports = { REGISTRY, getAction, resolveAction };
